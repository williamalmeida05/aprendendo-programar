/* Ex 06 - Faça um programa que imprima na tela se um nome é diferente ao outro nome digitado. Ex: João e João, imprime false. João e Maria, imprime true. */

//Igual
let firstName = "João"
let secondName = "João"



console.log(`Primeira setença é ${firstName != secondName}`)

//Diferente
let thirdName = "João"
let fourthName = "Maria"

console.log(`Segunda setença é ${thirdName != fourthName}`)